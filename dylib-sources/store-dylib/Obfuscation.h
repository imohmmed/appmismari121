// Declare a stack buffer and decrypt the XOR-encoded bytes into it.
// Usage:  XSTR(myVar, _ENC_SOME_STRING, _LEN_SOME_STRING)
//         // use myVar as const char *
//         XSTR_ZERO(myVar, _LEN_SOME_STRING)  // zero after use
#define XSTR(var, enc_array, len)                              \
    static const unsigned char _##var##_raw[] = enc_array;    \
    char var[(len)];                                           \
    do {                                                       \
        for (int _xi = 0; _xi < (int)(len) - 1; _xi++) {     \
            var[_xi] = (char)(_##var##_raw[_xi] ^ _XK);       \
        }                                                      \
        var[(len) - 1] = '\0';                                 \
    } while (0)

// Zero out the buffer after use (prevent memory scraping)
#define XSTR_ZERO(var, len)  memset((var), 0, (len))

